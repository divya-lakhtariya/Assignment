#include<stdio.h>
main()
{
  int a,b, ans;
  printf("enter value of a :");
  scanf("%d" ,&a);
  
  printf("enter value of b :");
  scanf("%d" ,&b);
  
  ans=a+b;
  
  printf("ANS : %d" ,ans);
  
  
  ans=a*b;
  
  printf("ANS : %d" ,ans);
  
  
  ans=a/b;
  
  printf("ANS : %d" ,ans);
  
  ans=a-b;
  
  printf("ANS : %d" ,ans);
    
}
